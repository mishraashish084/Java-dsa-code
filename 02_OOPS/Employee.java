import java.util.*;
public class Employee{
   private String name;
    private int rno;
    private int per;
public String getName()
{
    return name;
}
public void setName(String name)
{
    this.name=name;
}
public int getRoll()
{
    return rno;
}
public void  setRoll(int rno)
{
    this.rno=rno;
}
public int getPer()
{
    return per;
}
public void setPer(int per)
{
    this.per=per;
}
    
}